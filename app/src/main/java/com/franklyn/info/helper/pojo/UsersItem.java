public class UsersItem {

    private String usersImage, usersName, usersPraise, userCountry;

    public UsersItem() {
    }

    public String getUsersImage() {
        return usersImage;
    }

    public void setUsersImage(String usersImage) {
        this.usersImage = usersImage;
    }

    public String getUsersName() {
        return usersName;
    }

    public void setUsersName(String usersName) {
        this.usersName = usersName;
    }

    public String getUsersPraise() {
        return usersPraise;
    }

    public void setUsersPraise(String usersPraise) {
        this.usersPraise = usersPraise;
    }

    public String getUserCountry() {
        return userCountry;
    }

    public void setUserCountry(String userCountry) {
        this.userCountry = userCountry;
    }
}
